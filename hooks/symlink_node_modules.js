const fs = require('fs');
const path = require('path');

const projectRoot = path.join(__dirname, '..');
const studioMainPath = path.join(projectRoot, 'projects', 'studio-main');

/* May need administration authentication for windows to make symlink */
if (!fs.existsSync(path.join(studioMainPath, 'node_modules'))) {
  fs.symlinkSync(path.join(projectRoot, 'node_modules'), path.join(studioMainPath, 'node_modules'), 'dir');
}
